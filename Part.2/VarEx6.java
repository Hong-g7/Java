package part2;

import java.util.Scanner;

public class VarEx6 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println(a+b);

        String c = sc.nextLine();
        int i = Integer.parseInt(c);
        System.out.println(i);

    }
}
